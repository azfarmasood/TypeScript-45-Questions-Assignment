function countryin_city(city: string, country: string):void {
    console.log(`${city} is a city in ${country}`);
}

countryin_city("Karachi","Pakistan");


function countrywith_city(city: string, country: string):void {
    console.log(`${city} is a city in ${country}`);
}


countrywith_city("Istanbul","Turkey"); 
countrywith_city("Texas", "United States");
countrywith_city("Tokyo", "Japan");